# Variables with different types
x = 7        # int
y = 3.14     # float
z = "Python" # string

print("x is", x, "and its type is", type(x))
print("y is", y, "and its type is", type(y))
print("z is", z, "and its type is", type(z))

# Simple math
a = 5
b = 2
print(f"{a} + {b} = {a+b}")
print(f"{a} * {b} = {a*b}")
